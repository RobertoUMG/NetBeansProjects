    private void btn_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GuardarActionPerformed
        mdlTabla = new DefaultTableModel();
        
        String codigo = txt_Codigo.getText();
        int carne = Integer.parseInt(txt_Carne.getText());
        String nombre=txt_Nombre.getText();
        String dOB = txt_DOB.getText();
        int edad = Integer.parseInt(txt_Edad.getText());
        int ruta= Integer.parseInt(cbo_Ruta.getSelectedItem().toString());
        int salario = Integer.parseInt(txt_Salario.getText());
        
        vendedor.setCodigo(codigo);
        vendedor.setCarne(carne);
        vendedor.setNombre(nombre);
        vendedor.setDOB(dOB);
        vendedor.setEdad(edad);
        vendedor.setRuta(ruta);
        vendedor.setSalario(salario);
        
        metodos.guardar(vendedor);
        metodos.guardarArchivo(vendedor);
        
        JOptionPane.showMessageDialog(null, "Guardado exitosamente");
        
        
        txt_Codigo.setText(null);
        txt_Carne.setText(null);
        txt_Nombre.setText(null);
        txt_DOB.setText(null);
        txt_Edad.setText(null);
        txt_Salario.setText(null);
        cbo_Ruta.setSelectedIndex(0);
        
        
        
        
        
        
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_btn_GuardarActionPerformed

