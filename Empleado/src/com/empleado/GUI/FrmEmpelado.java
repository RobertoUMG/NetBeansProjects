    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:

        if(ebo.checkDuplicates(Integer.parseInt(txtIDEmpleado.getText())) == 0){
            JOptionPane.showMessageDialog(null,"ID ya existe, porfavor utilice otro ID");
            return;
        }

        if (txtIDEmpleado.getText().isEmpty() || txtNombres.getText().isEmpty() || txtApellidos.getText().isEmpty() || txtCedula.getText().isEmpty() ||txtEdad.getText().isEmpty() ) {
            JOptionPane.showMessageDialog(null, "Llene todos los campos ");            
        } else{
            
            char estado;
            if (rdCasado.isSelected()){
                estado = 'C';
            }else{
                estado = 'S';
            }
            
            char genero;
            if (rdFemenino.isSelected()){
                genero = 'F';
            }else{
                genero = 'M';
            }
            
            Empleado emp = new Empleado();
            emp.setIdEmpleado(Integer.parseInt(txtIDEmpleado.getText()));
            emp.setNombres(txtNombres.getText());
            emp.setApellidos(txtApellidos.getText());
            emp.setCedula(txtCedula.getText());
            emp.setEstadoCivil(estado);
            emp.setGenero(genero);
            emp.setEdad(Integer.parseInt(txtEdad.getText()));  
            String mensaje = ebo.agregarEmpleado(emp);
            JOptionPane.showMessageDialog(null, mensaje);
            limpiar();
            listarEmpleado();
            
        }
        
        
        
    }//GEN-LAST:event_btnAgregarActionPerformed

